interface WeightWithChange extends WeightLog {
  change: number
}

function weightHistorySection(logs: WeightLog[]): string {
  if (logs.length === 0) {
    return '<h2>Weight History</h2><p class="empty">No weight records.</p>'
  }
  const ascending = [...logs].sort((a, b) => a.date.localeCompare(b.date))
  const withChanges: WeightWithChange[] = ascending.map((log, i) => ({
    ...log,
    change: i > 0 ? log.weight - ascending[i - 1].weight : 0,
  }))
  withChanges.reverse()

  const rows = withChanges.map((w, i) => {
    const isLast = i === withChanges.length - 1
    let changeStr = ''
    let changeClass = ''
    if (!isLast) {
      const sign = w.change > 0 ? '+' : ''
      changeStr = `${sign}${w.change.toFixed(1)} ${w.weightUnit}`
      changeClass = w.change > 0 ? 'change-positive' : w.change < 0 ? 'change-negative' : ''
    }
    return `
      <tr>
        <td>${esc(formatDate(w.date))}</td>
        <td>${w.weight} ${esc(w.weightUnit)}</td>
        <td>${w.bodyConditionScore > 0 ? w.bodyConditionScore : ''}</td>
        <td class="${changeClass}">${changeStr}</td>
        <td>${esc(w.notes)}</td>
      </tr>
    `
  }).join('')

  return `
    <h2>Weight History</h2>
    <table>
      <tr><th>Date</th><th>Weight</th><th>Body Condition</th><th>Change</th><th>Notes</th></tr>
      ${rows}
    </table>
  `
}

function notesSection(notes: Note[]): string {
  const sorted = [...notes].sort((a, b) => {
    const aDate = tstampToDate(a.admin.created_at)?.getTime() ?? 0
    const bDate = tstampToDate(b.admin.created_at)?.getTime() ?? 0
    return bDate - aDate
  })
  if (sorted.length === 0) {
    return '<h2>Notes</h2><p class="empty">No notes.</p>'
  }
  const items = sorted.map(n => {
    const date = formatTstamp(n.admin.created_at)
    const tags = n.tags.length > 0 ? n.tags.join(', ') : ''
    return `
      <div style="margin-bottom: 10px; padding: 8px; border: 1px solid #eee; border-radius: 4px;">
        <div style="color: #666; font-size: 10px; margin-bottom: 4px;">
          ${esc(date)}${tags ? ` &mdash; ${esc(tags)}` : ''}
        </div>
        <div>${esc(n.text)}</div>
      </div>
    `
  }).join('')

  return `<h2>Notes</h2>${items}`
}

function withdrawalsSection(withdrawals: WithdrawalResult[]): string {
  if (withdrawals.length === 0) {
    return '<h2>Active Withdrawals</h2><p class="empty">No active withdrawals.</p>'
  }
  const rows = withdrawals.map(w => `
    <tr>
      <td>${esc(w.medicationName)}</td>
      <td>${esc(w.withdrawalType)}</td>
      <td>${esc(formatDate(w.withdrawalEndDate))}</td>
      <td class="withdrawal-active">${w.daysRemaining} days</td>
      <td class="withdrawal-active">${esc(w.status)}</td>
    </tr>
  `).join('')

  return `
    <h2>Active Withdrawals</h2>
    <table>
      <tr><th>Medication</th><th>Type</th><th>End Date</th><th>Days Remaining</th><th>Status</th></tr>
      ${rows}
    </table>
  `
}

function lineageSection(animal: Animal, animals: Animal[]): string {
  const sire = animal.sireId ? animals.find(a => a.id === animal.sireId) : null
  const dam = animal.damId ? animals.find(a => a.id === animal.damId) : null

  if (!sire && !dam) {
    return '<h2>Lineage</h2><p class="empty">No lineage information.</p>'
  }

  return `
    <h2>Lineage</h2>
    <table class="info-table">
      ${sire ? `<tr><td class="info-label">Sire</td><td class="info-value">${esc(sire.name)}${sire.register ? ` (#${esc(sire.register)})` : ''}</td></tr>` : ''}
      ${dam ? `<tr><td class="info-label">Dam</td><td class="info-value">${esc(dam.name)}${dam.register ? ` (#${esc(dam.register)})` : ''}</td></tr>` : ''}
    </table>
  `
}

function groupsSection(groups: AnimalGroup[]): string {
  if (groups.length === 0) {
    return '<h2>Group Memberships</h2><p class="empty">Not a member of any groups.</p>'
  }
  const items = groups.map(g => `<li>${esc(g.name)}</li>`).join('')
  return `<h2>Group Memberships</h2><ul style="margin: 8px 0 8px 20px;">${items}</ul>`
}

function wrapDocument(title: string, homesteadName: string, body: string): string {
  return `<!DOCTYPE html>
<html>
<head><meta charset="utf-8"><style>${styles()}</style></head>
<body>
  ${documentHeader(title, homesteadName)}
  ${body}
  ${footer()}
</body>
</html>`
}

export function buildCompleteRecordHtml(params: ExportParams): string {
  const body = [
    photoSection(params.photoBase64),
    generalInfoSection(params.animal, params.age),
    healthRecordsSection(params.healthRecords),
    careEventsSection(params.careEvents),
    breedingRecordsSection(params.breedingRecords, params.animals),
    productionLogsSection(params.productionLogs),
    weightHistorySection(params.weightLogs),
    notesSection(params.notes),
    withdrawalsSection(params.activeWithdrawals),
    lineageSection(params.animal, params.animals),
    groupsSection(params.groups),
  ].join('')
  return wrapDocument(`Complete Record — ${params.animal.name}`, params.homesteadName, body)
}

