function esc(text: string | number | undefined | null): string {
  if (text === undefined || text === null) return ''
  const s = String(text)
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;')
}

function formatTstamp(tstamp: CareEvent['dueDate'] | null | undefined): string {
  const date = tstampToDate(tstamp)
  if (!date) return ''
  return format(date, 'MMM d, yyyy')
}

